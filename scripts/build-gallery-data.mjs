import { copyFile, mkdir, readdir, readFile, rm, writeFile } from "node:fs/promises";
import path from "node:path";
import { imageSize } from "image-size";

const rootDir = process.cwd();
const publicDir = path.join(rootDir, "public");
const photosDir = path.join(publicDir, "photos");
const outputFile = path.join(rootDir, "src", "galleryData.js");
const supportedExtensions = new Set([".jpg", ".jpeg", ".png", ".webp"]);

const sourceDir = process.argv[2] ? path.resolve(process.argv[2]) : null;

if (sourceDir) {
  await rm(photosDir, { recursive: true, force: true });
  await copyImages(sourceDir, photosDir);
}

const files = (await walk(photosDir))
  .filter((file) => supportedExtensions.has(path.extname(file).toLowerCase()))
  .sort((a, b) => a.localeCompare(b));

if (files.length === 0) {
  throw new Error(`No supported image files found in ${photosDir}`);
}

const sections = await buildSections(files);

await writeFile(
  outputFile,
  `// Generated by scripts/build-gallery-data.mjs. Do not edit by hand.\nexport const sections = ${JSON.stringify(sections, null, 2)};\n`,
);

console.log(`Generated ${path.relative(rootDir, outputFile)} with ${files.length} photos.`);

async function copyImages(fromDir, toDir) {
  const sourceFiles = await walk(fromDir);

  for (const sourceFile of sourceFiles) {
    if (!supportedExtensions.has(path.extname(sourceFile).toLowerCase())) continue;

    const relativePath = path.relative(fromDir, sourceFile);
    const destination = path.join(toDir, relativePath);

    await mkdir(path.dirname(destination), { recursive: true });
    await copyFile(sourceFile, destination);
  }
}

async function walk(dir) {
  const entries = await readdir(dir, { withFileTypes: true });
  const found = [];

  for (const entry of entries) {
    if (entry.name.startsWith(".")) continue;

    const fullPath = path.join(dir, entry.name);

    if (entry.isDirectory()) {
      found.push(...(await walk(fullPath)));
    } else if (entry.isFile()) {
      found.push(fullPath);
    }
  }

  return found;
}

async function buildSections(photoFiles) {
  const grouped = new Map();

  for (const file of photoFiles) {
    const relative = path.relative(photosDir, file);
    const parts = relative.split(path.sep);
    const sectionSlug = parts.length > 1 ? parts[0] : "album";
    const parsed = parsePhotoName(path.basename(file, path.extname(file)));
    const dimensions = imageSize(await readFile(file));
    const tags = parsed.tags.length > 0 ? parsed.tags : [sectionSlug];

    const item = {
      src: publicPath(path.relative(publicDir, file)),
      width: dimensions.width,
      height: dimensions.height,
      alt: parsed.title,
      title: parsed.title,
      tags,
      date: parsed.date,
      dateLabel: formatDate(parsed.date),
      tone: toneForTags(tags),
    };

    if (!grouped.has(sectionSlug)) {
      grouped.set(sectionSlug, []);
    }

    grouped.get(sectionSlug).push(item);
  }

  return Array.from(grouped.entries()).map(([id, photos]) => {
    const sortedPhotos = photos.sort((a, b) => {
      const dateCompare = a.date.localeCompare(b.date);
      return dateCompare || a.title.localeCompare(b.title);
    });
    const uniqueTags = [...new Set(sortedPhotos.flatMap((photo) => photo.tags))].sort();
    const dates = [...new Set(sortedPhotos.map((photo) => photo.date).filter(Boolean))].sort();

    return {
      id,
      title: titleize(id),
      range: formatRange(dates),
      note: uniqueTags.map((tag) => `#${tag}`).join(" "),
      tags: uniqueTags,
      photos: sortedPhotos,
    };
  });
}

function parsePhotoName(name) {
  const dateMatch = name.match(/^(\d{4}-\d{2}-\d{2})/);
  const date = dateMatch?.[1] ?? "";
  const withoutDate = date ? name.slice(date.length).replace(/^-+/, "") : name;
  const [titlePart = name, ...tagParts] = withoutDate.split("--").filter(Boolean);
  const title = titleize(titlePart || name);
  const tags = tagParts.flatMap((part) => slugify(part).split("-")).filter(Boolean);

  return { date, title, tags };
}

function publicPath(relativePath) {
  return `/${relativePath.split(path.sep).map(encodeURIComponent).join("/")}`;
}

function titleize(value) {
  return value
    .split(/[-_\s]+/)
    .filter(Boolean)
    .map((word) => word.charAt(0).toUpperCase() + word.slice(1).toLowerCase())
    .join(" ");
}

function slugify(value) {
  return value
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-+|-+$/g, "");
}

function formatDate(value) {
  if (!value) return "";
  const [year, month, day] = value.split("-").map(Number);
  return new Intl.DateTimeFormat("en", { month: "short", day: "numeric", year: "numeric" }).format(
    new Date(year, month - 1, day),
  );
}

function formatRange(dates) {
  if (dates.length === 0) return "Undated";
  if (dates.length === 1) return formatDate(dates[0]);
  return `${formatDate(dates[0])} - ${formatDate(dates.at(-1))}`;
}

function toneForTags(tags) {
  if (tags.includes("animals")) return "blue";
  if (tags.includes("flowers")) return "tomato";
  if (tags.includes("nature")) return "green";
  return "soft";
}
